public class bitwise_operator {
    
    public static void main(String []args){

        System.out.println(5&6);
        System.out.println(2&4);
        System.out.println();

        System.out.println(6|4);
        System.out.println(4|6);
        System.out.println();

        System.out.println(6^4);
        System.out.println(4^6);
        System.out.println(3^6);
    }
}
